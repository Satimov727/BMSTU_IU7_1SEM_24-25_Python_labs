# –ü–µ—Ä–≤–æ–æ–±—Ä–∞–∑–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏–∏ f(x).
def F(x):
    return x * math.sin(x) + math.cos(x)

# –ú–µ—Ç–æ–¥ –ª–µ–≤—ã—Ö –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤.
def left_rectangle(f, a, b, n):
    n = float(n)
    if n == 0:
        return None
    
    h = (b - a) / n
    integral = 0
    n = int(n)
    for i in range(n):
        x = a + i * h
        integral += f(x) * h
        
    return integral

# –ú–µ—Ç–æ–¥ –ø–∞—Ä–∞–±–æ–ª.
def parabola(f, a, b, n):
    n = float(n)
    if n % 2 != 0 or n < 2:
        return None
    
    h = (b - a) / n
    integral = f(a) + f(b)
    
    # –°–ª–∞–≥–∞–µ–º–æ–µ —Å –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–æ–º 4.
    n = int(n)
    for i in range(1, n, 2):
        x = a + i * h
        integral += 4 * f(x)
    
    # –°–ª–∞–≥–∞–µ–º–æ–µ —Å –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–æ–º 2.
    for i in range(2, n-1, 2):
        x = a + i * h
        integral += 2 * f(x)
    
    return integral * h / 3

